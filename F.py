import turtle

# Set up the screen
screen = turtle.Screen()
screen.title("Turtle Drawing - Letter F")

# Create a turtle named 't'
t = turtle.Turtle()
t.hideturtle()
t.pensize(5)

t.fillcolor('blue')

t.begin_fill()

t.end_fill()

t.goto(0,300)
t.fd(200)
t.right(90)
t.fd(50)
t.right(90)
t.fd(150)
t.right(-90)
t.fd(50)
t.right(-90)
t.fd(100)
t.right(90)
t.fd(50)
t.right(90)
t.fd(100)
t.right(-90)
t.fd(50)
t.fd(100)
t.right(90)
t.fd(50)



t.speed(1)
t.penup()
t.fd(10000)
t.pendown()